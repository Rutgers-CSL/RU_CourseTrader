import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const RESEND_API_KEY = Deno.env.get("RESEND_API_KEY");

serve(async (req) => {
  // Handle CORS preflight
  if (req.method === "OPTIONS") {
    return new Response(null, {
      headers: {
        "Access-Control-Allow-Origin": "*",
        "Access-Control-Allow-Methods": "POST, OPTIONS",
        "Access-Control-Allow-Headers": "Content-Type, Authorization",
      },
    });
  }

  try {
    const { userEmail, courseName, sectionHave, sectionWant } = await req.json();

    const res = await fetch("https://api.resend.com/emails", {
      method: "POST",
      headers: {
        "Authorization": `Bearer ${RESEND_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        from: "RU Course Trader <onboarding@resend.dev>",
        to: userEmail,
        subject: `Swap Request Received: ${courseName}`,
        html: `
          <p>Hello,</p>
          <p>Your section swap request has been submitted.</p>
          <p><strong>Course:</strong> ${courseName}<br>
          <strong>Your section:</strong> ${sectionHave}<br>
          <strong>Section you want:</strong> ${sectionWant}</p>
          <p>We'll email you when we find a match!</p>
          <p>– RU Course Trader</p>
        `,
      }),
    });

    const data = await res.json();
    return new Response(JSON.stringify(data), {
      headers: { 
        "Content-Type": "application/json",
        "Access-Control-Allow-Origin": "*",
      },
      status: res.ok ? 200 : 400,
    });
  } catch (error) {
    return new Response(JSON.stringify({ error: error.message }), {
      headers: { "Access-Control-Allow-Origin": "*" },
      status: 500,
    });
  }
});

